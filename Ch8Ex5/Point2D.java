public class Point2D {

    //instance variables
    private int x;
    private int y;

    //setter and getter methods
    void setX(int x){
        this.x = x;
    }

    void setY(int y){
        this.y = y;
    }

    int getX(){
        return this.x;
    }

    int getY(){
        return this.y;
    }

}